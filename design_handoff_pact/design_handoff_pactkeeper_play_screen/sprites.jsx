const Sprite = ({ name, scale = 2, palette, data, style, className }) => {
  const sprite = data || SPRITES[name];
  const pal = palette || PALETTES[name.replace(/Tower$/, '')];
  if (!sprite) return null;
  const w = sprite[0].length;
  const h = sprite.length;
  return (
    <svg
      width={w * scale}
      height={h * scale}
      viewBox={`0 0 ${w} ${h}`}
      shapeRendering="crispEdges"
      style={{ imageRendering: 'pixelated', display: 'block', ...style }}
      className={className}
    >
      {sprite.map((row, y) =>
        row.split('').map((ch, x) => {
          const c = pal[ch];
          if (!c) return null;
          return <rect key={`${x}-${y}`} x={x} y={y} width="1" height="1" fill={c} />;
        })
      )}
    </svg>
  );
};

