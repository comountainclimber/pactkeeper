// Render a sigil as an SVG string (one <rect> per pixel). Caller can drop it
// straight into innerHTML.
export function renderSigilSvg(
  id: SigilId,
  accent: string,
  hi: string,
  glow: string,
  scale: number,
): string {
  const sprite = SIGILS[id];
  const w = sprite[0].length;
  const h = sprite.length;
  const pal: Record<string, string | null> = {
    ".": null,
    "1": "#0e0a08",
    "2": "#2a1a14",
    "3": accent,
    "4": hi,
    "5": glow,
  };

  let body = "";
  for (let y = 0; y < h; y++) {
    const row = sprite[y];
    for (let x = 0; x < w; x++) {
      const c = pal[row[x]];
      if (!c) continue;
      body += `<rect x="${x}" y="${y}" width="1" height="1" fill="${c}"/>`;
    }
  }
  return (
    `<svg width="${w * scale}" height="${h * scale}" viewBox="0 0 ${w} ${h}" ` +
    `shape-rendering="crispEdges" style="image-rendering:pixelated;display:block;">${body}</svg>`
  );
}
